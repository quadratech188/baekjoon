def main():
    net = []

    for _ in range(6):
        row = list(map(int, input().split()))

        if all(k == 0 for k in row):
            continue

        net.append(list(row))

    for start in range(6):
        if any(row[start] == 1 for row in net):
            break

    for end in range(5, -1, -1):
        if any(row[end] == 1 for row in net):
            break

    net = [row[start:end + 1] for row in net]

    if net in nets:
        print('yes')
    elif list(reversed(net)) in nets:
        print('yes')
    elif [list(reversed(col)) for col in net] in nets:
        print('yes')
    elif list(reversed([list(reversed(col)) for col in net])) in nets:
        print('yes')
    else:
        print('no')

for _ in range(3):
    main()
